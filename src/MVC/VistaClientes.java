    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if(rdcc.isSelected()){
        
        tipodocumento = 1;
        
        }else if(rdti.isSelected()){
        tipodocumento = 2;
        
        }

        try {
            nombres  = tflNombres.getText();
             apellidos = tflApellidos.getText();
             eps = tflEps.getText();
             alergias = tflAlergias.getText();
             fechaNacimiento = tflFechaNacimiento.getText();
             documento = Integer.parseInt(tflDocumento.getText());
             correo = tflCorreo.getText();
             estadoCivil = tflEstadoCivil.getText();   
             telefono = tflTelefono.getText();
             direccion  = tflDireccion.getText();
             
            // Instanciar la clase Agencias y enviar los datos
            Clientes cr = new Clientes();
            cr.create(tipodocumento, nombres, apellidos, eps, alergias, fechaNacimiento, correo, estadoCivil, telefono, direccion, documento);

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Error en los datos: Verifica los campos num√©ricos.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

